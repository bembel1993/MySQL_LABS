/* Таблица подразделений */
CREATE TABLE user02_DEPT
(
  DEPTNO INT NOT NULL,
  DNAME  VARCHAR(14) NOT NULL,
  LOC    VARCHAR(13)
);


ALTER TABLE user02_DEPT
  ADD CONSTRAINT user02_DEPT_PK PRIMARY KEY (DEPTNO);
ALTER TABLE user02_DEPT
  ADD CONSTRAINT user02_DEPT_UK UNIQUE (DNAME);

/* Таблица сотрудников */
CREATE TABLE user02_EMP
(
  EMPNO    INT NOT NULL,
  ENAME    VARCHAR(10) NOT NULL,
  JOB      VARCHAR(9),
  MGR      INT,
  HIREDATE DATE,
  SAL      INT,
  COMM     INT,
  DEPTNO   INT
);

ALTER TABLE user02_EMP
  ADD CONSTRAINT user02_EMP_PK PRIMARY KEY (EMPNO);
ALTER TABLE user02_EMP
  ADD CONSTRAINT user02_EMP_UK UNIQUE (ENAME);
ALTER TABLE user02_EMP
  ADD CONSTRAINT user02_EMP_DEPT_FK FOREIGN KEY (DEPTNO)
  REFERENCES user02_DEPT (DEPTNO);
ALTER TABLE user02_EMP
  ADD CONSTRAINT user02_EMP_MGR_FK FOREIGN KEY (MGR)
  REFERENCES user02_EMP (EMPNO);

  
